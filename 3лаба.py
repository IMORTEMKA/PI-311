"""Задача 7: Анализ 5 чисел"""

print("Введите 5 чисел:")
numbers = []
for i in range(5):
    num = float(input(f"Число {i+1}: "))
    numbers.append(num)

if len(numbers) == len(set(numbers)):
    print("Все числа уникальны")
else:
    print("Есть повторяющиеся числа")

even_count = 0
negative_count = 0
in_range_count = 0

for num in numbers:
    if num % 2 == 0:
        even_count += 1

    if num < 0:
        negative_count += 1

    if -256 <= num <= 1024:
        in_range_count += 1

print(f"Количество чётных чисел: {even_count}")
print(f"Количество отрицательных чисел: {negative_count}")
print(f"Количество чисел в промежутке [-256; 1024]: {in_range_count}")