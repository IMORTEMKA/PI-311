"""Задача 5: Простое число"""

def is_prime(n):
    if n <= 1:
        return False
    elif n == 2:
        return True
    elif n % 2 == 0:
        return False
    else:
        i = 3
        while i * i <= n:
            if n % i == 0:
                return False
            i += 2
        return True

n = int(input("Введите число: "))
if is_prime(n):
    print(f"{n} - простое число")
else:
    print(f"{n} - составное число")

